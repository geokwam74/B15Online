package day16;

public class WhileLoopPractice {
    public static void main(String[] args) {

        int x = 100;
        while (x<=105) {
            System.out.println("Hello World");
            ++x ;
          /*
          ++ iteration 1  x=100
          check 100 <=105 ------>> true
          ++ iteration 2  x=101
          check 101 <=105 ------>> true
          ++ iteration 3  x=102
          check 102 <=105 ------>> true
          ++ iteration 4  x=103
          check 103 <=105 ------>> true
          ++ iteration 5  x=104
          check 104 <=105 ------>> true
          ++ iteration 6  x=105
          check 106 <=105 ------>> true
          ++ iteration 7  x=104
          check 104 <=105 ------>> true

           */
        }

    }
}
