package day18;

public class ForLoop {

    public static void main(String[] args) {

        // count 1 to 5
        for (int i =1  ; i<=5; i++   ) {
            System.out.println(" i = " + i);
            System.out.println("Happy Birthday George");
        }


        for (int counter = 10 ; counter >0 ; counter --) {
            System.out.println("counter = " + counter);

        }
    }
}
