package day10;

public class Switch {


    public static void main(String[] args) {

        char operator ='+';
        switch (operator) {
            case '+':
                System.out.println("you are about to add numbers");
                break;
            case '-':
                System.out.println("you are about to substract numbers");
                break;
            case '*':
                System.out.println("you are about to multiple numbers");
                break;
            case '/':
                System.out.println("you are about to divided numbers");
                break;

        }

    }
}
